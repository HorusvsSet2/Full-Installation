menu_inicio() {
    mostrar_dibujo
    echo ""
    echo "======================== PANEL OSINT =========================="
    echo "======= EMAIL ============== USERS ============== MULTI ======="
    echo "     1) Holehe           3) Maigret          5) Mr. Holmes"
    echo "     2) Zehef            4) Sherlock         6) Argus"
    echo "                                             7) Recon-ng"
    echo -n
    echo "=========================== NMAP =============================="
    echo "     8) Comprobar conectividad a la IP"
    echo "     9) Escaneo de puertos"
    echo "     10) Escaneo de servicios"
    echo "     11) Escaneo de vulnerabilidades"
    echo "==============================================================="
    echo "12) Salir"
    echo -n
    echo -n "Selecciona una opción > "
    read opcion

    case "$opcion" in
        1) OSINT_holehe;;
        2) OSINT_zehef;;
        3) OSINT_maigret;;
        4) OSINT_sherlock;;
        5) OSINT_mrholmes;;
        6) OSINT_argus;;
        7) OSINT_reconng;;
        8) menu_nmap_ip;;
        9) menu_nmap_ports;;
        10) menu_nmap_services;;
        11) menu_nmap_vulns;;
        12) echo "Adiós."; exit 0;;
        *) echo "Opción no válida.";;
    esac
}

OSINT_holehe() {
    clear
    echo -n "Introduce un correo: "
    read correo
    holehe "$correo"
    read -p "Presiona ENTER para salir..."
}

OSINT_zehef() {
    clear
    echo -n "Introduce un correo: "
    read user
    zehef -u "$user"
    read -p "Presiona ENTER para salir..."
}

OSINT_maigret() {
    clear
    echo -n "Introduce nombre de usuario: "
    read user
    maigret "$user"
    read -p "Presiona ENTER para salir..."
}

OSINT_sherlock() {
    clear
    echo -n "Introduce nombre de usuario: "
    read user
    sherlock "$user"
    read -p "Presiona ENTER para salir..."
}

OSINT_mrholmes() {
    clear
    cd ~/Desktop/MultiTool/OSINT/Mr.Holmes
    source venv/bin/activate
    python3 MrHolmes.py
    read -p "Presiona ENTER para salir..."
}

OSINT_argus() {
    clear
    argus
    read -p "Presiona ENTER para salir..."
}

OSINT_reconng() {
    clear
    cd ~/Desktop/MultiTool/OSINT/recon-ng
    python3 recon-ng
    read -p "Presiona ENTER para salir..."
}

menu_nmap_ip() {
    clear
    mostrar_dibujo
    echo ""
    echo "====================== ESCANEO DE IP =========================="
    echo "  1) Verificación de IP abierta"
    echo "  2) Verificación de conectividad bidireccional"
    echo "  3) Visor de motivos de la conectividad"
    echo "  4) Atrás"
    echo -n
    echo -n "Selecciona una opción > "
    read opcion

    case "$opcion" in
        1) nmap_ip_1;;
        2) nmap_ip_2;;
        3) nmap_ip_3;;
        4) inicio;;
        *) echo "Opción no válida.";;
    esac
}

nmap_ip_1() {
    clear
    echo -n "Introduce la IP: "
    read IP
    sudo nmap "$IP" -sn
    read -p "Presiona ENTER para salir..."
}

nmap_ip_2() {
    clear
    echo -n "Introduce la IP: "
    read IP
    sudo nmap "$IP" -sn -PE
    read -p "Presiona ENTER para salir..."
}

nmap_ip_3() {
    clear
    echo -n "Introduce la IP: "
    read IP
    sudo nmap "$IP" -sn -PE --reason
    read -p "Presiona ENTER para salir..."
}

